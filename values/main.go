package main

import (
	"fmt"
)

func main() {
	fmt.Println("Hello, World!")

	fmt.Println("1+1=", 1+1)
	fmt.Println("4-2=", 4-2)

	fmt.Println("2*3=", 2*3)
	fmt.Println("8/2=", 8/2)
	fmt.Println("10.0/3.0=", 10.0/3.0)
	fmt.Println("5%2=", 5%2)

	fmt.Println("2^3=", 2^3)

	fmt.Println(true && false)
	fmt.Println(true || false)
	fmt.Println(!true)
}
